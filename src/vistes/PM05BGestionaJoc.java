    private void btInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInfoActionPerformed
        int fila = this.taula.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(this, "Cal seleccionar una fila", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Partida partida = modelTaulaPartides.getPartides().get(fila);
        String mensaje = "Dades de la partida seleccionada\n"
                + "NÃºmero de partida: " + partida.getNum_jornada() + "\n"
                + "Data inici: " + partida.getData_inici() + "\n"
                + "Data final: " + partida.getData_final() + "\n"
                + "Professor responsable: " + partida.getJoc().getProfessor();
        JOptionPane.showMessageDialog(this, mensaje, "Dades", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btInfoActionPerformed

